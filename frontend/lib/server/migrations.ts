type Queryable = {
  query: (text: string, values?: unknown[]) => Promise<{ rows: Array<Record<string, unknown>> }>;
};

type TransactionalQueryable = Queryable & {
  release?: () => void;
};

type PoolLike = Queryable & {
  connect?: () => Promise<TransactionalQueryable>;
};

type Migration = {
  id: string;
  description: string;
  statements: string[];
  run?: (client: Queryable) => Promise<void>;
};

