async function withClient<T>(
  pool: PoolLike,
  callback: (client: TransactionalQueryable) => Promise<T>,
) {
  if (!pool.connect) {
    return callback(pool);
  }

  const client = await pool.connect();
  try {
    return await callback(client);
  } finally {
    client.release?.();
  }
}

async function ensureMigrationTable(client: Queryable) {
  await client.query(`
    CREATE TABLE IF NOT EXISTS schema_migrations (
      id TEXT PRIMARY KEY,
      description TEXT NOT NULL,
      applied_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
    )
  `);
}

export async function getAppliedMigrationIds(client: Queryable) {
  await ensureMigrationTable(client);
  const result = await client.query(
    `SELECT id FROM schema_migrations ORDER BY applied_at ASC, id ASC`,
  );
  return new Set(result.rows.map((row) => String(row.id)));
}

export async function runMigrations(pool: PoolLike) {
  return withClient(pool, async (client) => {
    const appliedIds = await getAppliedMigrationIds(client);
    const executed: string[] = [];

    for (const migration of MIGRATIONS) {
      if (appliedIds.has(migration.id)) {
        continue;
      }

      await client.query("BEGIN");

      try {
        for (const statement of migration.statements) {
          await client.query(statement);
        }

        if (migration.run) {
          await migration.run(client);
        }

        await client.query(
          `
            INSERT INTO schema_migrations (id, description)
            VALUES ($1, $2)
          `,
          [migration.id, migration.description],
        );
        await client.query("COMMIT");
        executed.push(migration.id);
      } catch (error) {
        await client.query("ROLLBACK");
        throw error;
      }
    }

    return executed;
  });
}
